def download_benign_dataset(count=250):
    print(f"Downloading {count} benign extensions...")
    os.makedirs(settings.RAW_BENIGN_DIR, exist_ok=True)
    
    success = 0
    # First count how many are already downloaded
    for ext_id in POPULAR_EXTENSIONS:
        output_path = os.path.join(settings.RAW_BENIGN_DIR, f"{ext_id}.crx")
        if os.path.exists(output_path):
            success += 1

    print(f"Already have {success} downloaded benign extensions.")
    
    if success >= count:
        print(f"Already reached target of {count} benign extensions.")
        return

    # Iterate through all popular extensions to download more
    pbar = tqdm(total=count, initial=success, desc="Downloading Benign Extensions")
    for ext_id in POPULAR_EXTENSIONS:
        if success >= count:
            break
        output_path = os.path.join(settings.RAW_BENIGN_DIR, f"{ext_id}.crx")
        if os.path.exists(output_path):
            continue
        if download_crx(ext_id, settings.RAW_BENIGN_DIR):
            success += 1
            pbar.update(1)
            
    pbar.close()
    print(f"Successfully compiled {success} extensions in {settings.RAW_BENIGN_DIR}")

if __name__ == "__main__":
    download_benign_dataset(250)
