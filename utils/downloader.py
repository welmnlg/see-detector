def download_crx(extension_id, output_dir):
    """
    Downloads a CRX file directly from Google's Web Store update URL.
    """
    url = f"https://clients2.google.com/service/update2/crx?response=redirect&prodversion=126.0&acceptformat=crx2,crx3&x=id%3d{extension_id}%26uc"
    
    os.makedirs(output_dir, exist_ok=True)
    output_path = os.path.join(output_dir, f"{extension_id}.crx")
    
    # Skip if already downloaded
    if os.path.exists(output_path):
        return True
        
    try:
        # Create a request object with a User-Agent to avoid 403s
        req = urllib.request.Request(
            url, 
            headers={'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/126.0.0.0 Safari/537.36'}
        )
        with urllib.request.urlopen(req) as response, open(output_path, 'wb') as out_file:
            data = response.read()
            out_file.write(data)
        return True
    except Exception as e:
        print(f"Failed to download {extension_id}: {e}")
        return False

